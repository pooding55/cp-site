                <h2>The Bid, Ask and Spread</h2>
                <p>All  forex
                    quotes are quoted with two prices: the <strong>bid</strong> and <strong>ask</strong>.</p>
                <p>In general, the <strong>bid</strong> is lower than the <strong>ask</strong> price.</p>
                <p><img loading="lazy" class="aligncenter lazyload" title="EUR/USD forex quote" src="{{ Storage::url('images/pre-school-quote.png') }}" alt="EUR/USD forex quote" width="200" height="133"></p>
                <h3>What is “Bid”?</h3>
                <!--sse-->
                <p>The <strong>bid</strong> is the price at which your broker is willing to <em>buy</em> the base currency in
                    exchange for the quote currency.</p><!--/sse-->
                <p>This means the bid is the best available price at which you (the trader) can sell to the market.</p>
                <!--sse-->
                <p>If you want to sell something, the broker will buy it from you at the <strong>bid</strong> price.</p><!--/sse-->
                <h3>What is “Ask”?</h3>
                <p>The <strong>ask</strong> is the price at which your broker will <em>sell</em> the base currency in exchange for
                    the quote currency.</p>
                <p>This means the ask price is the best available price at which you can buy from the market.</p>
                <p>Another word for ask is the <strong>offer price.</strong></p>
                <p>If you want to buy something, the broker will sell (or offer) it to you at the <strong>ask</strong> price.</p>
                <h3>What is “Spread”?</h3>
                <p>The difference between the bid and the ask price is known as the <strong>SPREAD</strong>.</p>
